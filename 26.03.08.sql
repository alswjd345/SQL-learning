조인튜닝

Nested Loop Join은 한 테이블의 데이터를 한 행씩 읽으면서 다른 테이블에서 조건에 맞는 데이터를 찾는 방식이다
 이 방식은 데이터 양이 적고 인덱스가 존재할 때 성능이 좋지만, 반복적인 탐색 때문에 랜덤 액세스가 많이 발생할 수 있다
Sort Merge Join은 두 테이블을 조인 컬럼 기준으로 먼저 정렬한 뒤 정렬된 데이터를 순서대로 비교하면서 조인하는 방식이다
대량 데이터를 처리할 때 사용할 수 있지만 정렬 과정에서 추가적인 비용이 발생한다
Hash Join은 작은 테이블을 메모리에 해시 구조로 만든 후 큰 테이블의 데이터를 해시값으로 비교하면서 조인하는 방식이다
 대량 데이터 처리에 유리하며 정렬이 필요 없는 장점이 있다 

조인 튜닝을 할 때는 몇 가지 중요한 원칙이 있다
첫째 데이터 양이 적은 테이블을 먼저 조인하는 것이 좋다.
둘째 조인에 사용되는 컬럼에 인덱스를 생성하면 검색 속도를 향상시킬 수 있다
셋째 조인 전에 WHERE 조건을 이용해 불필요한 데이터를 줄이면 처리량을 줄일 수 있다
마지막으로 데이터 특성에 맞는 적절한 조인 방식을 선택하는 것이 중요하다       